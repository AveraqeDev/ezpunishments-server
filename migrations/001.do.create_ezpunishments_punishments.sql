CREATE TABLE ezpunishments_punishments (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY UNIQUE,
  name TEXT NOT NULL,
  reason TEXT NOT NULL,
  proof TEXT DEFAULT 'No Proof Uploaded',
  punished_by TEXT NOT NULL DEFAULT 'CONSOLE',
  removed_by TEXT DEFAULT NULL,
  active BOOLEAN NOT NULL DEFAULT TRUE,
  expires TIMESTAMP DEFAULT NULL,
  date_punished TIMESTAMP NOT NULL DEFAULT now(),
  updated TIMESTAMP DEFAULT NULL
);